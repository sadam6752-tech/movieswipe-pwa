console.log('🔧 Исправление стран в movies.json...\n');

try {
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  
  if (!data.movies || !Array.isArray(data.movies)) {
    console.log('⚠️  Неверный формат файла');
    process.exit(1);
  }

  let updated = 0;

  data.movies.forEach(movie => {
    const countries = MOVIE_COUNTRIES[movie.title];
    if (countries) {
      movie.countries = countries;
      updated++;
      console.log(`✅ ${movie.title} → ${countries.join(', ')}`);
    } else {
      // Оставляем как есть или ставим США по умолчанию для английских фильмов
      if (movie.language === 'en' || movie.originalTitle !== movie.title) {
        movie.countries = ['США'];
        updated++;
        console.log(`🔹 ${movie.title} → США (по умолчанию)`);
      }
    }
  });

  // Сохраняем
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8');

  console.log(`\n✨ Обновлено: ${updated} из ${data.movies.length} фильмов`);

} catch (error) {
  console.error('❌ Ошибка:', error.message);
  process.exit(1);
}
